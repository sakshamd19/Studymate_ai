def status_label(level: str) -> str:
    colors = {
        "weak": "red",
        "moderate": "yellow",
        "strong": "green",
        "high": "red",
        "low": "green",
    }
    color = colors.get(level, "yellow")
    return f":{color}[{level.upper()}]"


