def extract_ai_content(ai_resp) -> str:
    if isinstance(ai_resp, dict):
        return ai_resp.get("content", str(ai_resp))
    elif isinstance(ai_resp, str) and ai_resp.strip().startswith("{"):
        import json
        try:
            return json.loads(ai_resp).get("content", ai_resp)
        except Exception:
            return ai_resp
    return str(ai_resp) if ai_resp else ""


def render_practice_tab(report):
    st.markdown("### 📝 Readiness Assessment")
    st.caption("Generated by Assessment Agent")
    assessment = getattr(report, "assessment", None) or {}
    if not assessment:
        st.info("Assessment is being generated. Run the analysis first.")
        return

    final_output = assessment.get("final_output", "")
    st.write(final_output)


def render_reasoning_tab(report, wa, ra, ev):
    st.markdown("### 🧠 Multi-Agent Grounding & Evidence")
    
    agent_outputs = {
        "Learning Path Curator": getattr(report, "learning_path", {}),
        "Study Planner": getattr(report, "study_plan", {}),
        "Engagement Agent": getattr(report, "engagement_tracking", {}),
        "Assessment Agent": getattr(report, "assessment", {}),
        "Manager Insights Agent": getattr(report, "manager_insights", {}),
        "Weakness Analyzer": getattr(report, "weakness_analysis", {}),
        "Risk Predictor": getattr(report, "risk_assessment", {}),
        "Resource Finder": getattr(report, "resources", {}),
        "Career Readiness": getattr(report, "career_readiness", {}),
        "Counterfactual Reasoner": getattr(report, "counterfactuals", {}),
    }

    for agent_name, payload in agent_outputs.items():
        if not payload:
            continue
        trace = payload.get("reasoning_trace", [])
        evidence = payload.get("evidence", [])
        confidence = payload.get("confidence", 0) * 100
        
        with st.expander(f"⚙️ {agent_name} — Confidence: {confidence:.0f}%"):
            st.markdown("**Reasoning Trace:**")
            if trace:
                for step in (trace if isinstance(trace, list) else [trace]):
                    st.write(f"→ {step}")
            else:
                st.caption("No trace available")
                
            st.markdown("**Grounded Evidence:**")
            if evidence:
                for e in (evidence if isinstance(evidence, list) else [evidence]):
                    st.write(f"• {e}")
            else:
                st.caption("No evidence provided (Warning: Potential Hallucination)")

            if payload.get("why_this_decision"):
                st.caption(f"*Decision Logic:* {payload.get('why_this_decision')}")
    st.markdown("### 🤝 Multi-agent consensus")
    consensus = report.consensus or {}
    if consensus:
        c1, c2, c3 = st.columns(3)
        c1.metric("Agents engaged", consensus.get("agents_engaged", 0))
        c2.metric("Overall confidence", f"{consensus.get('overall_confidence', 0):.1f}%")
        c3.metric("Conflicts found", len(consensus.get("conflicts", consensus.get("disagreement_areas", []))))
        for area in consensus.get("consensus_areas", []):
            with st.expander(f"📊 {area.get('topic', 'Consensus area')}"):
                st.write(f"**Result:** {area.get('consensus', '')}")
                st.write(f"**Resolution:** {area.get('resolution', '')}")
                opinions = area.get("opinions", area.get("agent_opinions", []))
                for op in opinions:
                    conf = op.get("confidence", 0)
                    bar = "█" * int(conf / 10) + "░" * (10 - int(conf / 10))
                    st.write(f"• **{op.get('agent', 'Agent')}** [{bar}] {conf:.0f}%: {op.get('opinion', '')}")
    else:
        st.info("Run analysis to see agent consensus")

    st.markdown("### 🧐 Critic agent review")
    critic = report.critic_review or {}
    flaws = critic.get("flaws", [])
    improvements = critic.get("suggested_improvements", critic.get("improvements", []))
    if flaws:
        for flaw in flaws:
            st.warning(f"⚠️ {flaw}")
    else:
        st.success("✅ No critical flaws detected")
    if improvements:
        st.markdown("**Suggested improvements:**")
        for imp in improvements:
            st.write(f"• {imp}")

    st.markdown("### ⚡ Observability & latency")
    chain = ev.get("agent_execution_chain", report.observability.get("agent_execution_chain", []) if report.observability else [])
    if chain:
        total_ms = sum(step.get("latency_ms", 0) for step in chain)
        st.metric("Total pipeline latency", f"{total_ms:.2f}ms")
        col1, col2 = st.columns(2)
        for i, step in enumerate(chain):
            with (col1 if i % 2 == 0 else col2):
                st.metric(step.get("agent", "Agent"), f"{step.get('latency_ms', 0):.2f}ms")
    else:
        st.info("No observability data — run analysis first")

    st.markdown("### 📊 System self-evaluation")
    c1, c2, c3, c4 = st.columns(4)
    c1.metric("Reasoning", f"{ev.get('reasoning_quality_score', 0):.1f}%")
    c2.metric("Consistency", f"{ev.get('consistency_score', 0):.1f}%")
    c3.metric("Reliability", f"{ev.get('reliability_score', 0):.1f}%")
    c4.metric("Explainability", f"{ev.get('explainability_score', 0):.1f}%")
    for suggestion in ev.get("improvement_suggestions", []):
        st.caption(f"• {suggestion}")
    for d in ev.get("deductions", []):
        st.caption(f"• {d[0]}: {d[1]} points")


